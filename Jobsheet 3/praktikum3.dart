  /// Langkah 1
  for (int index = 10; index < 27; index++) {   /// Deklarasikan variabel 'index' dengan tipe data 'int'dan konsisten dalam penggunaannya 
                                                /// serta menambahkan operator increment
  /// Langkah 3
